#include <libc.h>

int main(int ac, char **av)
{
	int i = 0;
	int new;

	if (ac == 2)
	{
		while (av[1][i])
		{
			new = 1;
			if (av[1][i] >= 65 && av[1][i] <= 90)
				new = av[1][i] - 64;
			else if (av[1][i] >= 97 && av[1][i] <= 122)
				new = av[1][i] - 96;
			while (new >= 1)
			{
				write(1, &av[1][i], 1);
				new--;
			}
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}