#include <libc.h>

int main(int ac, char **av)
{
	int i = 0;
	int j = 0;
	int len = 0;

	if (ac == 3)
	{
		while(av[1][len])
			len++;
		while (av[1][i] && av[2][j])
		{
			if (av[1][i] == av[2][j])
				i++;
			j++;
		}
		if (i == len)
		{
			i = 0;
			while (av[1][i])
				write(1, &av[1][i++], 1);
		}
	}
	write(1, "\n", 1);
}
